def generate():
    # 256 chars, 8x8 pixels each
    atlas = np.zeros((8, 256 * 8), dtype=np.uint8)
    for code in range(256):
        pattern = FONT_8x8.get(code, [0]*8)
        # Flip/normalize if needed. For now just raw bits.
        for row in range(8):
            bits = pattern[row]
            for col in range(8):
                # MSB is left
                val = (bits >> (7 - col)) & 1
                atlas[row, code * 8 + col] = 255 if val else 0
    return atlas

