def generate_font_atlas(output_path):
    """
    Generate 2048x8 font atlas. Pure bitmap: 0x00=off, 0xFF=on.
    No anti-aliasing. True 8x8 pixel font.
    """
    atlas = bytearray(2048 * 8)  # 16384 bytes
    
    for char_id, rows in CP437.items():
        for row_idx, byte_val in enumerate(rows):
            for bit in range(8):
                px_x = char_id * 8 + bit
                px_y = row_idx
                # (1 << bit): LSB maps to leftmost pixel
                # This matches our GLSL/TouchDesigner rendering pipeline
                if byte_val & (1 << bit):
                    atlas[px_y * 2048 + px_x] = 0xFF
    
    with open(output_path, 'wb') as f:
        f.write(atlas)
    
    print(f"Font atlas: {output_path}")
    print(f"  True 8x8 bitmap (CP437), {len(atlas)} bytes")
    print(f"  Chars: 32-126 (A-Z, a-z, 0-9, symbols)")

